package tostring;

/**
 * Режим, определяющий, должен ли элемент участвовать
 * в формировании строкового представления.
 */
public enum Mode {

    /**
     * Элемент включается в строковое представление.
     */
    YES,

    /**
     * Элемент исключается из строкового представления.
     */
    NO
}
