package two;

import java.lang.annotation.*;

/**
 * Аннотация для задания двух обязательных параметров —
 * строкового и числового. Применяется к типам и может
 * использоваться для передачи метаданных, необходимых
 * для обработки во время выполнения.
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface Two {

    /**
     * Строковое значение, связанное с аннотацией.
     *
     * @return строковый параметр
     */
    String one();

    /**
     * Числовое значение, связанное с аннотацией.
     *
     * @return числовой параметр
     */
    int two();
}
